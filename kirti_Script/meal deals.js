let container=document.getElementById("food")

function append(data){
    container.innerHTML=null
  data.forEach(function(el){
      let div=document.createElement("div")
      div.addEventListener("click",function(){
        window.location.href = "recipe.html"
      })

      let name=document.createElement("h2")
      name.innerText=el.name
      let image=document.createElement("img")
      image.setAttribute('class','everyimg');
      image.src=el.image
      let Restaurant_name=document.createElement("p")
      
      Restaurant_name.innerText=`Restaurant:-  ${el.Restaurant_name}`


      let adress=document.createElement("h4")
      adress.innerText=el.adress
      let state=document.createElement("h4")
      state.innerText=el.state

      div.append(name,image,Restaurant_name,adress,state)
    container.append(div)
})
}  
